    {
//      dd($request->all());
        $data = $request->validate([
            'first_name'=>'required',
            'last_name'=>'required',
            'gender'=>'required',
            'email'=>'required|email',
            'phone_number'=>'required',
            'major'=>'required',

            ]);
        Student::create($data);
       return redirect()->back();
    }

